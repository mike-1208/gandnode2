namespace Grand.Domain.Catalog;

/// <summary>
///     Represents a backorder mode
/// </summary>
public enum BackorderMode
{
    /// <summary>
    ///     No backorders
    /// </summary>
    NoBackorders = 0,

    /// <summary>
    ///     Allow qty below 0
    /// </summary>
    AllowQtyBelowZero = 1
}